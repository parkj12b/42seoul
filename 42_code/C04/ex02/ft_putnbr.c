#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	n;
	char	buf[11];
	int		i;

	i = 0;
	n = nb;
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (nb < 0)
		n *= -1;
	while (n > 0)
	{
		buf[i++] = n % 10 + '0';
		n /= 10;
	}
	if (nb < 0)
		write(1, "-", 1);
	while (i > 0)
		write(1, &buf[--i], 1);
}
