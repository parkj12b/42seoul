void	ft_rev_int_tab(int *tab, int size)
{
	int	n;
	int	i;
	int	temp;

	i = 0;
	n = size / 2;
	while (n > i)
	{
		temp = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = temp;
		i++;
	}
}
