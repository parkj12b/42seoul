#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;

	argc--;
	while (argc > 0)
	{
		i = 0;
		while (*(argv[argc] + i) != '\0')
			i++;
		write(1, argv[argc], i);
		argc--;
		write(1, "\n", 1);
	}
}
