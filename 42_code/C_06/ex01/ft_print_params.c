#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	*c;

	i = 1;
	if (argc == 1)
		return (0);
	while (i < argc)
	{
		c = argv[i];
		while (*c != '\0')
		{
			write(1, c, 1);
			c++;
		}
		i++;
		write(1, "\n", 1);
	}
}
